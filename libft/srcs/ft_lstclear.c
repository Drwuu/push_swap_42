#include "../includes/libft.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*save;

	if (!*lst)
		return ;
	while (*lst)
	{
		save = (*lst)->next;
		ft_lstdelone(lst);
		*lst = save;
	}
	*lst = NULL;
}
